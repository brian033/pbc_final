def random_partition(total, parts):
    # 生成（parts-1）個隨機分割點，並加上0和total
    dividers = sorted(random.sample(range(1, total), parts - 1))
    dividers = [0] + dividers + [total]

    # 計算各段的長度，即每份的大小
    partition = [dividers[i+1] - dividers[i] for i in range(parts)]

    return partition


def generate_random_alphanumeric_string(length):
    # 定義英文字母和數字的字符集合
    characters = string.ascii_letters + string.digits
    # 從字符集合中隨機選取指定數量的字符並組成字符串
    random_string = ''.join(random.choice(characters) for _ in range(length))
    return random_string


